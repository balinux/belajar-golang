package main

import "fmt"

func main() {
	var name string

	name = "rio"
	fmt.Println(name)

	name = "rio juniyantara putra"
	fmt.Println(name)

	// deklarasi awal
	country := "indonesia"
	fmt.Println(country)

	country = "usa"
	fmt.Println(country)

	var (
		firstname = "rio"
		lastname  = "juniyantara"
	)

	fmt.Println(firstname)
	fmt.Println(lastname)
}
